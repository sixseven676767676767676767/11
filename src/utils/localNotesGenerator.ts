// Helper to determine if a title corresponds to a preset matching key
export function getPresetMatchKey(title: string): string | null {
  const t = title.toLowerCase();
  if (t.includes("黑洞") || t.includes("relative") || t.includes("相對論")) return "blackhole";
  if (t.includes("cpu") || t.includes("二進位") || t.includes("binary")) return "cpu";
  if (t.includes("供需") || t.includes("經濟學") || t.includes("demand")) return "economics";
  if (t.includes("永續") || t.includes("碳中和") || t.includes("energy") || t.includes("carbon")) return "energy";
  if (t.includes("行為") || t.includes("展望") || t.includes("prospect")) return "prospect";
  if (t.includes("心臟") || t.includes("心電圖") || t.includes("ecg") || t.includes("cardio")) return "ecg";
  if (t.includes("5g") || t.includes("邊緣運算") || t.includes("edge")) return "edge";
  if (t.includes("藍海") || t.includes("blue ocean")) return "blueocean";
  if (t.includes("共價鍵") || t.includes("軌域") || t.includes("atomic") || t.includes("orbit")) return "chem";
  return null;
}

